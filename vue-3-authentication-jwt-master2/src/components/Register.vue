<template>
  <div class="col-md-12">
    <div class="card card-container">
      <img
        id="profile-img"
        src="//ssl.gstatic.com/accounts/ui/avatar_2x.png"
        class="profile-img-card"
      />
      <Form @submit="handleRegister" :validation-schema="schema">
        <div v-if="!successful">
         <div class="form-group">
            <label for="nombre">Nombre</label>
            <Field name="nombre" type="text" class="form-control" />
            <ErrorMessage name="nombre" class="error-feedback" />
          </div>
          <div class="form-group">
            <label for="nombreUsuario">Nombre de Usuario</label>
            <Field name="nombreUsuario" type="text" class="form-control" />
            <ErrorMessage name="nombreUsuario" class="error-feedback" />
          </div>
          <div class="form-group">
            <label for="email">Email</label>
            <Field name="email" type="email" class="form-control" />
            <ErrorMessage name="email" class="error-feedback" />
          </div>
          <div class="form-group">
            <label for="password">Contrase침a</label>
            <Field name="password" type="password" class="form-control" />
            <ErrorMessage name="password" class="error-feedback" />
          </div>

          <div class="form-group">
            <button class="btn btn-primary btn-block" :disabled="loading">
              <span
                v-show="loading"
                class="spinner-border spinner-border-sm"
              ></span>
              Sign Up
            </button>
          </div>
        </div>
      </Form>

      <div
        v-if="successful"
        class="alert"
        :class="successful ? 'alert-success' : 'alert-danger'"
      >
        {{ message }}
      </div>
    </div>
  </div>
</template>

<script>
import { Form, Field, ErrorMessage } from "vee-validate";
import * as yup from "yup";

export default {
  name: "Register",
  components: {
    Form,
    Field,
    ErrorMessage,
  },
  data() {
    const schema = yup.object().shape({
      nombre: yup
        .string()
        .required("Nombre obligatorio")
        .min(3, "Debe tener al menos 3 caracteres"),
      nombreUsuario: yup
        .string()
        .required("Nombre de usuario obligatorio")
        .min(3, "Debe tener al menos 3 caracteres"),
      email: yup
        .string()
        .required("Email obligatorio")
        .email("Email incorrecto")
        .max(50, "M치ximo 50 caracteres"),
      password: yup
        .string()
        .required("Contrase침a obligatorio")
        .max(40, "M치ximo 40 caracteres"),
    });

    return {
      successful: false,
      loading: false,
      message: "",
      schema,
    };
  },
  computed: {
    loggedIn() {
      return this.$store.state.auth.status.loggedIn;
    },
  },
  created() {
    if (this.loggedIn) {
      this.$router.push("/profile");
    }
  },
  methods: {
    handleRegister(user) {
      this.message = "";
      this.successful = false;
      this.loading = true;

      this.$store.dispatch("auth/register", user).then(
        () => {
          this.message = "usuario creado correctamente";
          this.successful = true;
          this.loading = false;
        },
        (error) => {
          this.message =
            (error.response &&
              error.response.data &&
              error.response.data.message) ||
            error.message ||
            error.toString();
          this.successful = false;
          this.loading = false;
        }
      );
    },
  },
};
</script>

